	/**
	* Used for unsigned integer promotion where differences (max value - min value) in set values can result in
	* exceeding the total range/ of the underlying type.
	*/
	template <class V>
	requires fuzzy::numeric<V>
	constexpr auto promote(V v)
	{
		if constexpr (std::is_floating_point_v<V> || std::is_unsigned_v<V>)
			return v;
		else if constexpr (sizeof(int) > sizeof(V))
			return static_cast<int>(v);
		else if constexpr (sizeof(long) > sizeof(V))
			return static_cast<long>(v);
		else if constexpr (sizeof(long long) > sizeof(V))
			return static_cast<long long>(v);
		else
			return v;
	}

	template <class M>
	requires std::floating_point<M>
	constexpr M fma(M x, M y, M z) noexcept
	{
		if (std::is_constant_evaluated()) // FUTURE: Replace this with 'if consteval' in C++23.
			return (x * y) + z;
		else
			return std::fma(x,y,z);
	}

	/**
	 * @brief Calculates the absolute value of a floating point type.
	 * @tparam M Must be floating point.
	 * @param m The value to find the absolute of.
	 * @return The absolute value.
	*/
	template <class M>
	requires std::floating_point<M>
	constexpr M abs(M m) noexcept
	{
		// FUTURE: Replace this in C++23 for std::abs which will have a better implementation.
		return ((m < static_cast<M>(0)) || (m == static_cast<M>(-0.0))) ? -m : m;
	}

	/**
	* Performs rounding of floating point membership type only if value is integral, otherwise performs a simple static conversion.
	* @param number The number to round.
	* @return The 'rounded' number (which will not actually be rounded for floating point value types).
	*/
	template <class V, class M>
	requires fuzzy::numeric<V> && std::floating_point<M>
	constexpr M round(M number) noexcept
	{
		if constexpr (std::is_floating_point_v<V>)
		{
			return static_cast<M>(number);
		}
		else
		{
			if (std::is_constant_evaluated()) // FUTURE: Replace this with C++23 std::round
			{
				if (number >= static_cast<M>(0))
					return static_cast<M>(static_cast<long long>(number + static_cast<M>(0.5l)));
				else
					return static_cast<M>(static_cast<long long>(number - static_cast<M>(0.5l)));
			}
			else
			{
				return std::round(number);
			}
		}
	}

	namespace detail
	{
		template <class M>
		requires std::floating_point<M>
		constexpr float round_off() noexcept
		{
			if constexpr (sizeof(M) == 4)
				return 0.0000077f; // Loss of 7 bits ulp

			return 0.00000000000015; // Loss of 7 bits ulp
		}
	}


	/**
	 * @brief A fudged version of operator== for floating point types in the range [0,1].
	 * @tparam M Must be a floating point type.
	 * @param m0 First argument.
	 * @param m1 Second argument.
	 * @return Whether the two arguments are equivelant.
	*/
	template <class M>
	requires std::floating_point<M>
	constexpr bool equivelant(M m0, M m1, M ro = detail::round_off<M>()) noexcept
	{
		M const abs = fuzzy::math::abs(m1 - m0);
		return abs <= ro;
	}

	/**
	 * @brief A thin wrapper around operator== for floating point types in the range [0,1].
	 * @tparam M Must be a floating point type.
	 * @param m0 First argument.
	 * @param m1 Second argument.
	 * @return Whether the two arguments are equivelant.
	*/
	template <class V>
	requires std::integral<V>
	constexpr bool equivelant(V v0, V v1) noexcept
	{
		return v0 == v1;
	}

	/**
	* Strict linear interpolation based on line segment from two points at the point 'key', which must be 
	* in in the range [lhs, rhs].
	* @param lhs Element with lower value.
	* @param key The value at which to interpolate a membership value.
	* @param rhs Element with greater value.
	* @return The linear interpolated membership.
	*/
	template <class V, class M>
	requires fuzzy::numeric<V> && std::floating_point<M>
	constexpr M linear_interpolate(basic_element<V, M> lhs, V key, basic_element<V, M> rhs) noexcept
	{
		assert (lhs.value() <= key && key <= rhs.value());
		M dy = rhs.membership() - lhs.membership();
		M dx = static_cast<M>(promote(rhs.value()) - promote(lhs.value()));
		M x_offset = static_cast<M>(promote(key) - promote(lhs.value()));
		M denom_inv = static_cast<M>(1) / dx;
		M ratio = x_offset * denom_inv;
		M const result =  fma(ratio, dy, lhs.membership());
		return result;
	}

	/**
	 * @brief Type used for modeling segments of two elements intersect.
	 * @tparam V The value type.
	 * @tparam M The membership type.
	*/
	template <class V, class M>
	requires fuzzy::numeric<V> && std::floating_point<M>
	struct basic_segment
	{
		using element_t = basic_element<V, M>;
		element_t v0;
		element_t v1;
	};

	/**
	 * @brief Determines the intersection point between two segments.
	 * @tparam V The value type.
	 * @tparam M The membership type.
	 * @param s0 The first segment.
	 * @param s1 The second segment.
	 * @return The point of inersection.
	*/
	template <class V, class M>
	requires fuzzy::numeric<V> && std::floating_point<M>
	constexpr std::optional<basic_element<V, M>> intersection(basic_segment<V, M> const& s0, basic_segment<V, M> const& s1)
	{
		using element_t = basic_element<V, M>;
		assert(s0.v0.value() < s0.v1.value());
		assert(s1.v0.value() < s1.v1.value());

		M const s0_deltax = static_cast<M>(s0.v1.value() - s0.v0.value());
		M const s0_deltay = s0.v1.membership() - s0.v0.membership();
		M const s1_deltax = static_cast<M>(s1.v1.value() - s1.v0.value());
		M const s1_deltay = s1.v1.membership() - s1.v0.membership();
		M const s0_s1_v_offset = static_cast<M>(s0.v0.value() - s1.v0.value());
		M const s0_s1_m_offset = s0.v0.membership() - s1.v0.membership();

		M const s_num = fma(s0_deltax, s0_s1_m_offset, -s0_deltay * s0_s1_v_offset);
		M const t_num = fma(s1_deltax, s0_s1_m_offset, -s1_deltay * s0_s1_v_offset);
		M const denom = fma(s0_deltax, s1_deltay,      -s0_deltay * s1_deltax);
		if (denom == static_cast<M>(0))
			return std::optional<element_t>{};

		M const inv_denom = static_cast<M>(1) / denom;
		M const s = s_num * inv_denom;
		M const t = t_num * inv_denom;

		M constexpr eps = std::numeric_limits<M>::epsilon();
		if (s >= -eps && s <= static_cast<M>(1) + eps && t >= -eps && t <= static_cast<M>(1) + eps)
		{
			V v_rounded = static_cast<V>(fuzzy::math::round<V>(fma(t, s0_deltax, static_cast<M>(s0.v0.value()))));
			V const v_clamp_min = std::max(s0.v0.value(), s1.v0.value());
			V const v_clamp_max = std::min(s0.v1.value(), s1.v1.value());

			if (v_rounded < v_clamp_min || fuzzy::math::equivelant(v_rounded, v_clamp_min) ||
			   (v_clamp_max < v_rounded) || fuzzy::math::equivelant(v_clamp_max, v_rounded))
			{
				return std::optional<element_t>{};
			}
			V const v = std::clamp(static_cast<V>(v_rounded), v_clamp_min, v_clamp_max);

			M const t_v = [&]() -> M
			{
				if constexpr (std::integral<V>)
				{
					M const v_inv_denom = static_cast<M>(1) / s0_deltax;
					M const delta_v = v - s0.v0.value();
					return delta_v * v_inv_denom;
				}
				else
				{
					return t;
				}
			}();

			M const m = fma(t_v, s0_deltay, s0.v0.membership());
			assert(m == m);
			return element_t{ v, m };
		}

		return std::optional<element_t>{};
	}

